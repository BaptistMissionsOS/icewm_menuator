          IconButton(
            icon: const Icon(Icons.restart_alt),
            tooltip: 'Reload IceWM Menu',
            onPressed: _isLoading ? null : _reloadIceWMMenu,
          ),
        ],
      ),
      body: _buildBody(),
    );
  }

  Widget _buildBody() {
    if (_isLoading) {
      return const Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            CircularProgressIndicator(),
            SizedBox(height: 16),
            Text('Loading ~/.icewm/menu...'),
          ],
        ),
      );
    }

    if (_errorMessage != null) {
      return Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            const Icon(Icons.error_outline, size: 48, color: Colors.red),
            const SizedBox(height: 16),
            Padding(
              padding: const EdgeInsets.symmetric(horizontal: 32.0),
              child: Text(
                _errorMessage!,
                textAlign: TextAlign.center,
                style: const TextStyle(fontSize: 14),
              ),
            ),
            const SizedBox(height: 16),
            ElevatedButton.icon(
              onPressed: _loadMenuFile,
              icon: const Icon(Icons.refresh),
              label: const Text('Retry'),
            ),
          ],
        ),
      );
    }

    if (_menuEntries.isEmpty) {
      return Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            const Icon(Icons.inbox, size: 48, color: Colors.grey),
            const SizedBox(height: 16),
            const Text('No menu entries found'),
            const SizedBox(height: 16),
            ElevatedButton.icon(
              onPressed: () => _onAddEntry(EntryType.prog),
              icon: const Icon(Icons.terminal),
              label: const Text('Add First Entry'),
            ),
          ],
        ),
      );
    }

    return Row(
      children: [
        // Left panel: Menu tree
        Expanded(
          flex: 2,
          child: Container(
            decoration: BoxDecoration(
              border: Border(
                right: BorderSide(
                  color: Theme.of(context).dividerColor,
                ),
              ),
            ),
            child: SingleChildScrollView(
              child: MenuTreeWidget(
                entries: _menuEntries,
                onEntrySelected: _onEntrySelected,
                onEntryDropped: _onEntryDropped,
                selectedEntry: _selectedEntry,
                onEntryUpdated: _onEntryUpdated,
              ),
            ),
          ),
        ),
        // Right panel: Entry editor
        Expanded(
          flex: 1,
          child: EntryEditorWidget(
            selectedEntry: _selectedEntry,
            onEntryUpdated: _onEntryUpdated,
            onEntryDeleted: _onEntryDeleted,
            onEntryUnhidden: _onEntryUnhidden,
            onAddEntry: _onAddEntry,
            onAddSubdirectory: _onAddSubdirectory,
            onMoveUp: (entry) {
              moveEntryUp(entry);
              ScaffoldMessenger.of(context).showSnackBar(
                const SnackBar(
                  content: Text('Moved up'),
                  duration: Duration(seconds: 1),
                ),
              );
            },
            onMoveDown: (entry) {
              moveEntryDown(entry);
              ScaffoldMessenger.of(context).showSnackBar(
                const SnackBar(
                  content: Text('Moved down'),
                  duration: Duration(seconds: 1),
                ),
              );
            },
            onClearSelection: () {
              setState(() {
                _selectedEntry = null;
              });
            },
            onMoveToSubmenu: (IceSubMenu submenu) {
              if (_selectedEntry != null) {
                _onEntryDropped(_selectedEntry!, submenu);
              }
            },
            availableSubmenus: _getAllSubmenus(),
          ),
        ),
      ],
    );
  }

  @override
  void dispose() {
    _liveUpdateTimer?.cancel();
    super.dispose();
  }
}
